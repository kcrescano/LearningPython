# Additional Data type 
# Boolean either True or False

boolean = not True 
# not invert value, true becomes false
boolean = True and True # True
boolean = True and False # False
# and return false if atleast 1 false value
boolean = True or False # True
boolean = False or False # False
# or returns True if atleast 1 true value

# Truthy and falsy values
# falsy: None, False, 0, 0.0 or empty string/list: "", (), []
# Others are more likely Truthy values, like: 1, "1", True, [1]
